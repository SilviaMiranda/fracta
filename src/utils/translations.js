/**
 * Get translation for current language
 * @param {string} lang - Language code
 * @param {string} key - Translation key
 * @param {object} params - Parameters to replace in translation (e.g., {n: 5})
 * @returns {string} - Translated string
 */
export const t = (lang, key, params = {}) => {
  const translation = translations[lang]?.[key] || translations.en[key] || key;
  
  // Replace parameters in translation (e.g., "Question {n}" -> "Question 5")
  return translation.replace(/\{(\w+)\}/g, (match, paramKey) => {
    return params[paramKey] !== undefined ? params[paramKey] : match;
  });
};

/**
 * Get all translations for a language
 * @param {string} lang - Language code
 * @returns {object} - All translations for the language
 */
export const getTranslations = (lang) => {
  return translations[lang] || translations.en;
};

